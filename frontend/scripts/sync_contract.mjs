import { createHash } from "node:crypto";
import { existsSync, readFileSync, writeFileSync } from "node:fs";
import path from "node:path";

function resolvePaths() {
  const cwd = process.cwd();
  const frontendDir = existsSync(path.join(cwd, "frontend", "package.json"))
    ? path.join(cwd, "frontend")
    : cwd;

  const backendDirCandidates = [
    path.join(cwd, "backend"),
    path.join(cwd, "..", "backend"),
    path.join(frontendDir, "..", "backend"),
  ];

  const backendDir = backendDirCandidates.find((candidate) =>
    existsSync(path.join(candidate, "server.py"))
  );

  if (!backendDir) {
    throw new Error(
      "Could not locate backend directory containing server.py. " +
        "Ensure this repository contains both frontend/ and backend/."
    );
  }

  return { frontendDir, backendDir };
}

function readApiVersion(serverPath) {
  const serverSource = readFileSync(serverPath, "utf8");
  const match = serverSource.match(/API_VERSION\s*=\s*["']([^"']+)["']/);
  return match ? match[1].trim() : "";
}

function sha256OfFile(filePath) {
  const data = readFileSync(filePath);
  return createHash("sha256").update(data).digest("hex");
}

function main() {
  const { frontendDir, backendDir } = resolvePaths();
  // Canonical location after the dataset reorg; keep the legacy path as
  // a read-only fallback for anyone with pre-reorg artefacts.
  const predictionsPathNew = path.join(
    backendDir, "dataset", "predictions", "predictions_2026.csv"
  );
  const predictionsPathLegacy = path.join(backendDir, "predictions_2026.csv");
  const predictionsPath = existsSync(predictionsPathNew)
    ? predictionsPathNew
    : predictionsPathLegacy;
  const serverPath = path.join(backendDir, "server.py");

  if (!existsSync(predictionsPath)) {
    throw new Error(
      `Missing predictions_2026.csv (checked ${predictionsPathNew} and ${predictionsPathLegacy}). ` +
        "Generate predictions first (python backend/train.py) before deploying frontend."
    );
  }

  const detectedHash = sha256OfFile(predictionsPath);
  const detectedApiVersion = readApiVersion(serverPath);

  const expectedHash =
    process.env.VITE_EXPECTED_PREDICTIONS_SHA256?.trim().toLowerCase() ||
    detectedHash;
  const expectedApiVersion =
    process.env.VITE_EXPECTED_API_VERSION?.trim() || detectedApiVersion;

  const outputPath = path.join(frontendDir, ".env.production.local");
  const outputLines = [
    "# Auto-generated by frontend/scripts/sync_contract.mjs",
    `VITE_EXPECTED_PREDICTIONS_SHA256=${expectedHash}`,
    `VITE_EXPECTED_API_VERSION=${expectedApiVersion}`,
    "",
  ];

  writeFileSync(outputPath, outputLines.join("\n"), "utf8");

  console.log(
    `[sync_contract] wrote ${outputPath} with hash=${expectedHash.slice(0, 12)}... api_version=${expectedApiVersion || "n/a"}`
  );
}

try {
  main();
} catch (error) {
  const message = error instanceof Error ? error.message : String(error);
  console.error(`[sync_contract] ${message}`);
  process.exit(1);
}
